    /**
     * 根据阻抗值查找对应的编码值
     * 
     * @param impedanceValue 阻抗值
     * @return 对应的编码值，如果找不到则返回原值
     */
    fun mapImpedanceValue(impedanceValue: Long): Long {
        val key = impedanceValue.toString()
        return de2EnDictionary[key] ?: impedanceValue
    }

    /**
     * 批量映射阻抗值
     * 
     * @param dataRows CSV数据行列表
     * @return 映射后的数据行列表
     */
    fun mapImpedanceValues(dataRows: List<CSVFIleUtil.CSVDataRow>): List<CSVFIleUtil.CSVDataRow> {
        return dataRows.map { dataRow ->
            dataRow.copy(
                impedance50Khz = mapImpedanceValue(dataRow.impedance50Khz),
                impedance100Khz = mapImpedanceValue(dataRow.impedance100Khz)
            )
        }
    }

    /**
     * 检查字典中是否包含指定的阻抗值
     * 
     * @param impedanceValue 阻抗值
     * @return 是否包含该值
     */
    fun containsImpedanceValue(impedanceValue: Long): Boolean {
        return de2EnDictionary.containsKey(impedanceValue.toString())
    }

